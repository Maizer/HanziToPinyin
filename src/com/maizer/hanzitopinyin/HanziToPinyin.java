	public static final char MIN_HANZI = '\u4E00';
	public static final char MAX_HANZI = '\u9fa5';

	public Object[] getPinyin(String input, ResultRuler rule) {
		InputStream inputStream = null;

		Object[] objs = new Object[2];
		objs[0] = false;
		String[] data = new String[input.length()];
		StringBuilder content = new StringBuilder(6);
		int length = input.length();
		int count = 0;
		for (int i = 0; i < length; i++) {
			char letter = Character.toUpperCase(input.charAt(i));
			int skipCount = letter - MIN_HANZI;
			if (skipCount >= 0 && letter <= MAX_HANZI) {
				if (content.length() != 0) {
					data[count] = content.toString();
					content.setLength(0);
					count++;
				}
				int result = Collections.binarySearch(AMEND, letter + "", AMEND_BIN_COMP);
				if (result >= 0) {
					objs[0] = true;
					String[] mList = AMEND.get(result);
					for (int j = 1; j < mList.length; j++) {
						String mStr = mList[j];
						content.append((j == 1 ? "" : " ") + mStr);
					}
				} else {
					try {
						inputStream = mContext.getResources().openRawResource(R.raw.hanzi);
						inputStream.skip(skipCount * 6);
						int code;
						while ((code = inputStream.read()) != -1 && code != ' ') {
							content.append(Character.toUpperCase((char) code));
						}
					} catch (IOException e) {
						content.append(letter);
					} finally {
						close(inputStream);
					}
				}

			} else if ((letter < '0' || letter > '9') && (letter < 'A' || letter > 'Z') && rule != null
					&& !rule.isEnable(letter)) {
				if (content.length() != 0) {
					data[count] = content.toString();
					content.setLength(0);
					count++;
				}
				content.append(" ");
			} else {
				content.append(letter);
				continue;
			}
			data[count] = content.toString();
			content.setLength(0);
			count++;
		}

		if (content.length() != 0) {
			data[count] = content.toString();
			content.setLength(0);
			count++;
		}

		if (count != data.length) {
			String[] newData = new String[count];
			System.arraycopy(data, 0, newData, 0, count);
			data = newData;
		}
		objs[1] = data;
		return objs;
	}

	public String getPinyin(String input, ResultRuler limit, String spac) {
		InputStream inputStream = null;

		StringBuilder sb = new StringBuilder(6);
		StringBuilder content = new StringBuilder(6);
		int length = input.length();

		for (int i = 0; i < length; i++) {
			char letter = Character.toUpperCase(input.charAt(i));
			int skipCount = letter - MIN_HANZI;

			if (skipCount >= 0 && letter <= MAX_HANZI) {
				if (content.length() != 0) {
					sb.append(content.toString());
					sb.append(spac);
					content.setLength(0);
				}
				int result = Collections.binarySearch(AMEND, letter + "", AMEND_BIN_COMP);
				if (result >= 0) {
					String[] mList = AMEND.get(result);
					content.append(mList[1]);
					for (int j = 2; j < mList.length; j++) {
						content.append(" ");
						content.append(mList[j]);
					}
				} else {
					try {
						inputStream = mContext.getResources().openRawResource(R.raw.hanzi);
						inputStream.skip(skipCount * 6);
						int code;
						while ((code = inputStream.read()) != -1 && code != ' ') {
							content.append(Character.toUpperCase((char) code));
						}
					} catch (IOException e) {
						content.append(letter);
					} finally {
						close(inputStream);
					}
				}
			} else if ((letter < '0' || letter > '9') && (letter < 'A' || letter > 'Z') && limit != null
					&& !limit.isEnable(letter)) {
				if (content.length() != 0) {
					sb.append(content.toString());
					sb.append(spac);
					content.setLength(0);
				}
				content.append(" ");
			} else {
				content.append(letter);
				continue;
			}
			sb.append(content.toString());
			sb.append(spac);
			content.setLength(0);
		}

		if (content.length() != 0) {
			sb.append(content.toString());
			sb.append(spac);
			content.setLength(0);
		}
		return sb.toString();

	}

