	public static class ResultRuler {

		public static final ResultRuler S_RULER = new ResultRuler();

		public boolean isEnable(char c) {
			return Character.isLetterOrDigit(c);
		}

		public String getStringMapFromNumber(char c) {
			switch (c) {
			case '2':
				return "ABC";
			case '3':
				return "DEF";
			case '4':
				return "GHI";
			case '5':
				return "JKL";
			case '6':
				return "MNO";
			case '7':
				return "PQRS";
			case '8':
				return "TUV";
			case '9':
				return "WXYZ";
			}
			return null;
		}

		public char getLetterMap(char c) {
			if (c >= 65 && c <= 90) {
				if (c <= 67)
					return '2';
				else if (c <= 70)
					return '3';
				else if (c <= 73)
					return '4';
				else if (c <= 76)
					return '5';
				else if (c <= 79)
					return '6';
				else if (c <= 83)
					return '7';
				else if (c <= 86)
					return '8';
				else if (c <= 90)
					return '9';
			}
			return ' ';
		}

	}

