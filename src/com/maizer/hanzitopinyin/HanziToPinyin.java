	public static String[] getPinYinT9Number(String[] pinyins, ResultRuler ruleNumber) {

		if (ruleNumber == null) {
			ruleNumber = ResultRuler.S_RULER;
		}

		pinyins = pinyins.clone();
		StringBuilder sb = new StringBuilder();
		int length;
		String letters;
		char zeor = Character.toUpperCase('0');
		char nine = Character.toUpperCase('9');

		for (int i = 0; i < pinyins.length; i++) {
			letters = pinyins[i];
			length = letters.length();
			for (int j = 0; j < length; j++) {
				char c = letters.charAt(j);
				if (c < zeor || c > nine) {
					c = ruleNumber.getLetterMap(c);
				}
				sb.append(c);
			}
			pinyins[i] = sb.toString();
			sb.setLength(0);
		}
		return pinyins;
	}

