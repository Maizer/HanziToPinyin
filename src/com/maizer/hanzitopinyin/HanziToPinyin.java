	public static final class Pinyin implements Comparable<Pinyin> {
		public final String[] pinyin;
		public final String[] number;
		public final boolean more;
		public final char initial;

		private boolean hasMore(String[] pinyin) {
			for (int i = 0; i < pinyin.length; i++) {
				if (pinyin[i].indexOf(" ") > 0) {
					return true;
				}
			}
			return false;
		}

		public Pinyin(boolean hasNumber, String[] pinyin, ResultRuler limitnumber) {
			this.more = hasMore(pinyin);
			this.pinyin = pinyin;
			this.initial = pinyin[0].charAt(0);
			this.number = hasNumber ? getPinYinT9Number(pinyin, limitnumber) : null;
		}

		public Pinyin(boolean hasNumber, boolean more, String[] pinyin, ResultRuler limitnumber) {
			this.more = more;
			this.pinyin = pinyin;
			this.initial = pinyin[0].charAt(0);
			this.number = hasNumber ? getPinYinT9Number(pinyin, limitnumber) : null;
		}

		@Override
		public int compareTo(Pinyin o) {
			String[] pinyin1 = pinyin;
			String[] pinyin2 = o.pinyin;
			if (pinyin1 == null || pinyin1.length == 0) {
				if ((pinyin2 != null && pinyin2.length != 0)) {
					return -1;
				}
				return 0;
			} else if ((pinyin2 == null || pinyin2.length == 0)) {
				return 1;
			}
			for (int i = 0; i < pinyin1.length && i < pinyin2.length; i++) {
				String p1 = pinyin1[i];
				String p2 = pinyin2[i];
				if (more) {
					int index1 = p1.indexOf(" ");
					if (index1 > 0) {
						p1 = p1.substring(0, index1);
					}
				}
				if (o.more) {
					int index2 = p2.indexOf(" ");
					if (index2 > 0) {
						p2 = p2.substring(0, index2);
					}
				}
				int result = p1.compareTo(p2);
				if (result != 0) {
					return result;
				}
			}
			if (pinyin1.length > pinyin2.length) {
				return 1;
			}
			if (pinyin2.length > pinyin1.length) {
				return -1;
			}
			if (more && !o.more) {
				return 1;
			}
			if (!more && o.more) {
				return -1;
			}
			return 0;
		}

	}

